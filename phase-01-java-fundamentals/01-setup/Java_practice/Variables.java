package Java_practice;

public class Variables {
    public static void main (String args[]){

        int age = 22;
        System.out.println(age);

        double age1  = 22;
        System.out.println(age1);

        char age3 = 'A';
        System.out.println(age3);

        boolean age4 = false;
        System.out.println(age4);

        String name = "aryan";
        System.out.println(name);

    }
}
